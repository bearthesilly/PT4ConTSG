"""
TelecomTS Segment dataset with JSON-based segment captions.

This dataset supports segment-level text annotations where each time series
has 4 segment descriptions plus a global summary.

Key differences from standard datasets:
- Captions are stored in JSON format (not .npy)
- Each sample has segment_texts (List[str]) and summary (str)
- Data is pre-normalized, so normalize=False must be used
- For training: uses summary as condition (via cap_emb)
- For evaluation: segment_texts available for segment-level metrics
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Dict, List, Optional

import numpy as np
import torch
from torch.utils.data import Dataset

from contsg.config.schema import DataConfig
from contsg.data.datamodule import BaseDataModule, TimeSeriesDataset
from contsg.registry import Registry


class TelecomTSSegmentDataset(Dataset):
    """
    Dataset for TelecomTS with segment-level captions from JSON.

    Expected file structure:
        data_folder/
        ├── meta.json
        ├── train_ts.npy              # (N, 128, 2) - pre-normalized
        ├── train_segment_caps.json   # [{"segment_texts": [...], "summary": ...}, ...]
        ├── train_caps.npy            # (N,) summaries - generated by precompute
        ├── train_cap_emb.npy         # (N, D) embeddings - generated by precompute
        └── [same for valid_*, test_*]

    Attributes:
        n_segments: Number of segments per time series (default: 4)
        segment_len: Length of each segment (default: 32)
    """

    N_SEGMENTS = 4
    SEGMENT_LEN = 32

    def __init__(
        self,
        data_folder: Path,
        split: str = "train",
        normalize: bool = False,  # Data is pre-normalized
        transform: Optional[Any] = None,
        load_segment_texts: bool = True,
        provide_bridge_example: bool = False,
    ):
        """
        Initialize the dataset.

        Args:
            data_folder: Path to dataset folder
            split: Data split ("train", "valid", "test")
            normalize: Whether to normalize (should be False for this dataset)
            transform: Optional transform to apply
            load_segment_texts: Whether to load segment_texts from JSON
            provide_bridge_example: Whether to provide bridge_example_ts for Bridge model
        """
        self.data_folder = Path(data_folder)
        self.split = split
        self.normalize = normalize
        self.transform = transform
        self.load_segment_texts = load_segment_texts
        self.provide_bridge_example = provide_bridge_example

        if normalize:
            import warnings
            warnings.warn(
                "TelecomTSSegmentDataset: normalize=True but data is pre-normalized. "
                "Setting normalize=False to avoid double normalization."
            )
            self.normalize = False

        self._load_data()

    def _load_data(self) -> None:
        """Load time series and captions from files."""
        prefix = self.split

        # Load time series (pre-normalized)
        ts_path = self.data_folder / f"{prefix}_ts.npy"
        if not ts_path.exists():
            raise FileNotFoundError(f"Time series file not found: {ts_path}")
        self.ts = np.load(ts_path).astype(np.float32)

        # Load segment captions from JSON
        self.segment_caps_data: Optional[List[Dict[str, Any]]] = None
        self.caps: Optional[np.ndarray] = None
        self.segment_texts_list: Optional[List[List[str]]] = None

        json_path = self.data_folder / f"{prefix}_segment_caps.json"
        if json_path.exists():
            with open(json_path, "r", encoding="utf-8") as f:
                self.segment_caps_data = json.load(f)

            # Extract summaries as caps
            self.caps = np.array(
                [entry["summary"] for entry in self.segment_caps_data],
                dtype=object
            )

            # Extract segment_texts if needed
            if self.load_segment_texts:
                self.segment_texts_list = [
                    self._pad_segment_texts(entry.get("segment_texts", []))
                    for entry in self.segment_caps_data
                ]

        # Also try loading pre-computed summary caps (for fallback)
        caps_npy_path = self.data_folder / f"{prefix}_caps.npy"
        if caps_npy_path.exists() and self.caps is None:
            self.caps = np.load(caps_npy_path, allow_pickle=True)

        # Load pre-computed embeddings if available
        self.cap_emb: Optional[np.ndarray] = None
        cap_emb_path = self.data_folder / f"{prefix}_cap_emb.npy"
        if cap_emb_path.exists():
            self.cap_emb = np.load(cap_emb_path).astype(np.float32)

        # Try benchmark format embeddings
        if self.cap_emb is None:
            import glob
            pattern = str(self.data_folder / f"{prefix}_cap_emb_*.npy")
            matches = glob.glob(pattern)
            if matches:
                # Prefer 1024-dim if multiple exist
                for match in matches:
                    if "1024" in match:
                        self.cap_emb = np.load(match).astype(np.float32)
                        break
                if self.cap_emb is None:
                    self.cap_emb = np.load(matches[0]).astype(np.float32)

        # Load attributes if available (optional)
        self.attrs: Optional[np.ndarray] = None
        attrs_path = self.data_folder / f"{prefix}_attrs_idx.npy"
        if attrs_path.exists():
            self.attrs = np.load(attrs_path)

        # Load labels if available (optional)
        self.labels: Optional[np.ndarray] = None
        labels_path = self.data_folder / f"{prefix}_labels.npy"
        if labels_path.exists():
            self.labels = np.load(labels_path)

    def _pad_segment_texts(self, segment_texts: List[str]) -> List[str]:
        """
        Pad or truncate segment_texts to exactly N_SEGMENTS.

        This ensures compatibility with CTTPModel's segment encoder.
        """
        if len(segment_texts) >= self.N_SEGMENTS:
            return segment_texts[:self.N_SEGMENTS]
        else:
            # Pad with empty strings
            return segment_texts + [""] * (self.N_SEGMENTS - len(segment_texts))

    def __len__(self) -> int:
        return len(self.ts)

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        """
        Get a single sample.

        Returns:
            Dictionary containing:
                - "ts": Time series tensor (L, C)
                - "tp": Time positions (L,)
                - "cap": Summary text (str)
                - "summary": Same as cap (str)
                - "cap_emb": Caption embedding (D,) if available
                - "segment_texts": List of 4 segment descriptions
                - "idx": Sample index
        """
        ts_tensor = torch.from_numpy(self.ts[idx])
        seq_len = ts_tensor.shape[0]

        item: Dict[str, Any] = {
            "ts": ts_tensor,
            "tp": torch.arange(seq_len, dtype=torch.float32),
            "idx": idx,
        }

        # Add summary as cap (for model training)
        if self.caps is not None:
            summary = str(self.caps[idx])
            item["cap"] = summary
            item["summary"] = summary

        # Add pre-computed embedding
        if self.cap_emb is not None:
            item["cap_emb"] = torch.from_numpy(self.cap_emb[idx])

        # Add segment texts for evaluation
        if self.segment_texts_list is not None:
            item["segment_texts"] = self.segment_texts_list[idx]

        # Add attributes if available
        if self.attrs is not None:
            item["attrs"] = torch.from_numpy(self.attrs[idx])

        # Add label if available
        if self.labels is not None:
            item["label"] = torch.tensor(self.labels[idx])

        # Add example time series for Bridge model
        if self.provide_bridge_example:
            # Randomly sample a different index (avoid information leakage)
            example_idx = idx
            while example_idx == idx:
                example_idx = np.random.randint(0, len(self))
            item["bridge_example_ts"] = torch.from_numpy(self.ts[example_idx])

        # Apply transform
        if self.transform:
            item = self.transform(item)

        return item


@Registry.register_dataset("telecomts_segment")
class TelecomTSSegmentDataModule(BaseDataModule):
    """
    DataModule for TelecomTS Segment dataset.

    This dataset contains telecom time series (RSRP, UL_SNR) with
    segment-level text annotations. Each 128-timestep series is
    divided into 4 segments of 32 timesteps each.

    Training uses summary text as condition.
    Evaluation can access segment_texts for segment-level metrics.

    Important:
        - Data is pre-normalized, set normalize=False in config
        - Requires pre-computed cap_emb.npy for training
    """

    def _create_dataset(self, split: str) -> Dataset:
        """Create dataset for the given split."""
        # CTTP segment training needs segment_texts in train split as well.
        model_name = self.train_config.get("model_name", "")
        load_segment_texts = split in ["valid", "test"] or model_name.lower() == "cttp"

        # Check if Bridge model requires example time series
        provide_bridge_example = model_name.lower() == "bridge"

        return TelecomTSSegmentDataset(
            data_folder=self.config.data_folder,
            split=split,
            normalize=self.config.normalize,
            load_segment_texts=load_segment_texts,
            provide_bridge_example=provide_bridge_example,
        )
